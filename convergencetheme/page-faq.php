                      <a href="javascript:void(0)" data-role="toggleLink" role="button" class="toggleLink" data-toggle="collapse" data-target="#49d8bea7-aeb7-6f8d-8b39-ff00008e632a" aria-expanded="false" aria-controls="49d8bea7-aeb7-6f8d-8b39-ff00008e632a">
                        <h3> Are students of all Christian denominations welcome? What about other religious traditions? </h3>
                      </a>
                      <div style="display:none;" class="collapse multi-collapse"> Everyone is welcome to apply, though applicants should understand that the program is designed to look at the convergence of Christian theology with science. Included in the schedule are Christian worship services at a United Methodist Church, a visit to a Presbyterian seminary, and the creation of worship resources by our youth for a Christian worship service they will lead on their last Sunday, and to be shared with other churches to use in their worship services. Students from Protestant, Catholic, and Orthodox traditions are welcome, from established denominations and non-denominational traditions. Parents should be comfortable with their children &ldquo;going deep&rdquo; in both science and theology, learning from college-level professors, and challenging stereotypes often found in many schools and many faith communities. </div>
                    </li>
