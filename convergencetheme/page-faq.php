                        <h3> How will my child be supervised while they are at the camp? </h3>
                      </a>
                      <div style="display:none;" class="collapse multi-collapse"> In addition to the camp directors, faculty leaders, and other staff support, we will hire a current Seminary student to serve as Head Counselor. Additionally, Randolph-Macon College students serve as Counselors during the 3-day event. The Head Counselor and Counselors will reside in the dorm on the same hall as the campers and will accompany campers to lectures, meals, and all activities. We limit enrollment to 40 campers to ensure an appropriate staff to camper ratio. </div>
                    </li>
                    <li class="expandlistitem">
                      <a href="javascript:void(0)" data-role="toggleLink" role="button" class="toggleLink" data-toggle="collapse" data-target="#6dd8bea7-aeb7-6f8d-8b39-ff00008e632a" aria-expanded="false" aria-controls="6dd8bea7-aeb7-6f8d-8b39-ff00008e632a">
                        <h3> Are the dorms safe? </h3>
                      </a>
                      <div style="display:none;" class="collapse multi-collapse"> Yes! Campers will be housed in freshman dorms and our campus is very safe. Randolph-Macon College dormitories are secured 24 hours a day and are only accessible with an electronic entrance key; each participant will be issued an entrance key when they register for camp. Additionally, the Office of Campus Safety performs regular patrols of the dormitories and campus 24 hours a day. </div>
                    </li>
                    <li class="expandlistitem">
                      <a href="javascript:void(0)" data-role="toggleLink" role="button" class="toggleLink" data-toggle="collapse" data-target="#7fd8bea7-aeb7-6f8d-8b39-ff00008e632a" aria-expanded="false" aria-controls="7fd8bea7-aeb7-6f8d-8b39-ff00008e632a">
                        <h3> Can my child choose his/her roommate if he/she registers with a friend? </h3>
                      </a>
                      <div style="display:none;" class="collapse multi-collapse"> You may indicate a roommate choice and we will try to honor those choices, but we cannot guarantee that all roommate preferences will be fulfilled. </div>
                    </li>
                    <li class="expandlistitem">
                      <a href="javascript:void(0)" data-role="toggleLink" role="button" class="toggleLink" data-toggle="collapse" data-target="#91d8bea7-aeb7-6f8d-8b39-ff00008e632a" aria-expanded="false" aria-controls="91d8bea7-aeb7-6f8d-8b39-ff00008e632a">
                        <h3> What if there is an emergency? How can I contact my child? </h3>
                      </a>
                      <div style="display:none;" class="collapse multi-collapse"> You will be provided with an emergency contact number so that you can reach a staff person 24 hours a day. Your child may also bring his/her/their cell phone, if s/he/they has one, although students will sign a code of conduct indicating that they will only use their phones during free time. </div>
                    </li>
                    <li class="expandlistitem">
                      <a href="javascript:void(0)" data-role="toggleLink" role="button" class="toggleLink" data-toggle="collapse" data-target="#a3d8bea7-aeb7-6f8d-8b39-ff00008e632a" aria-expanded="false" aria-controls="a3d8bea7-aeb7-6f8d-8b39-ff00008e632a">
                        <h3> My child regularly takes medication. Can s/he participate in this program? </h3>
                      </a>
                      <div style="display:none;" class="collapse multi-collapse"> Each camper must complete a medical information form and any medications must be noted in detail (size and frequency of dose.) Campers will be responsible for keeping and administering their own medications. No staff members are allowed to administer any medications to your child, whether prescription or over-the-counter. RMC Campus Safety is on campus 24 hours a day for medical emergencies, but there is not a camp nurse or other medical personnel. </div>
                    </li>
                    <li class="expandlistitem">
                      <a href="javascript:void(0)" data-role="toggleLink" role="button" class="toggleLink" data-toggle="collapse" data-target="#b5d8bea7-aeb7-6f8d-8b39-ff00008e632a" aria-expanded="false" aria-controls="b5d8bea7-aeb7-6f8d-8b39-ff00008e632a">
                        <h3> What if my child is vegetarian/vegan/gluten-free has a food allergy etc.? </h3>
                      </a>
                      <div style="display:none;" class="collapse multi-collapse"> The majority of meals during Convergence will be eaten in Estes Dining Hall on the Randolph-Macon College campus. Our dining hall can easily accommodate special dietary concerns. Of course, our directors need to know of dietary concerns in order to ensure that all dining experiences will meet your child&rsquo;s needs. When you complete the permission forms during the registration process, please indicate any special dietary needs. </div>
                    </li>
                    <li class="expandlistitem">
                      <a href="javascript:void(0)" data-role="toggleLink" role="button" class="toggleLink" data-toggle="collapse" data-target="#adcfc1a7-aeb7-6f8d-8b39-ff00008e632a" aria-expanded="false" aria-controls="adcfc1a7-aeb7-6f8d-8b39-ff00008e632a">
                        <h3> Is my child required to have the COVID-19 vaccine to attend camp? </h3>
                      </a>
                      <div style="display:none;" class="collapse multi-collapse">
                        <p>While we cannot require your child to take the COVID-19 vaccine to attend camp, we do take health and safety of any kind very seriously. If your child is vaccinated, please share this with us by sending a copy of their vaccination records. If they are not, we will provide a COVID test for them via mail before camp, and during registration on July 15th. This helps us ensure all campers are safe during their stay. If your child tests positive during registration or at any time during their stay at RMC, we ask that you come pick them up immediately, so we may continue to operate as safely as possible.</p>
